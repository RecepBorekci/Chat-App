class UserData {
  final String name;
  final String username;
  final String phoneNumber;
  final String email;
  final String password;
  final String? profilePictureURL;

  UserData({
    required this.name,
    required this.username,
    required this.phoneNumber,
    required this.email,
    required this.password,
    required this.profilePictureURL
  });
}
